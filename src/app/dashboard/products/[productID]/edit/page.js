async function getCategories() {
  const res = await fetch(baseurl + `/categories`);
  const data = await res.json();
  return data;
}



const Page = async () => {
  const category = await getCategories();


  return (
    <>
      <Dashboardlayout>
        <ProductForm data={category} />
      </Dashboardlayout>
    </>
  );
};

export default Page;
